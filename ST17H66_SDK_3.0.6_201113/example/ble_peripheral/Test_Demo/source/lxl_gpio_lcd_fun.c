/*****************************************************************************
/                                      《温度转换成华氏温度》 
/*****************************************************************************/
u8 lxl_ht_c_f_count(u8 tick , u8 vlaue)
{
	u16 temp = 0 ; 
	if(tick){    //温度转换成华氏温度
		temp = vlaue*18 ;
		temp = temp/10+32 ;
	}else {
		temp = (vlaue-32)*10 ;
		temp = temp/18 ;
	}
	return temp ;
}
/*****************************************************************************
/                                      《HEX转BCD处理》 16-10
/*****************************************************************************/
u16 lxl_data_HEX_into_BCD(u8 HEX)
{
    u16 BCD=0;

    while(HEX>=10)
    {
        HEX-=10;
        BCD++;
    }
    return ((u16)(BCD<<4)|HEX);
}



